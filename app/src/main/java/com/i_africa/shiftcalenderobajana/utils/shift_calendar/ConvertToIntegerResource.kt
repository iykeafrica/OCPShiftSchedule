package com.i_africa.shiftcalenderobajana.utils.shift_calendar

import com.i_africa.shiftcalenderobajana.R

object ConvertToIntegerResource {
    fun convertColorIntToResources(colorResource: Int): Int {
        var integerResource = 0

        if (colorResource == 1)
            integerResource = R.color.color1
        if (colorResource == 2)
            integerResource = R.color.color2
        if (colorResource == 3)
            integerResource = R.color.color3
        if (colorResource == 4)
            integerResource = R.color.color4
        if (colorResource == 5)
            integerResource = R.color.color5
        if (colorResource == 6)
            integerResource = R.color.color6
        if (colorResource == 7)
            integerResource = R.color.color7
        if (colorResource == 8)
            integerResource = R.color.color8
        if (colorResource == 9)
            integerResource = R.color.color9
        if (colorResource == 10)
            integerResource = R.color.color10
        if (colorResource == 11)
            integerResource = R.color.color11
        if (colorResource == 12)
            integerResource = R.color.color12
        if (colorResource == 13)
            integerResource = R.color.color13
        if (colorResource == 14)
            integerResource = R.color.color14
        if (colorResource == 15)
            integerResource = R.color.color15
        if (colorResource == 16)
            integerResource = R.color.color16

        return integerResource
    }

}